def rename_columns(df, lang="EN"):
    """Rename DataFrame columns AND index to friendly display names based on language."""
    col_map = COLUMN_NAMES.get(lang, COLUMN_NAMES["EN"])
    rename_dict = {}
    for col in df.columns:
        if col in col_map:
            rename_dict[col] = col_map[col]
    result = df.rename(columns=rename_dict) if rename_dict else df
    # Also rename index if it has a name in the mapping
    if result.index.name and result.index.name in col_map:
        result.index.name = col_map[result.index.name]
    return result
