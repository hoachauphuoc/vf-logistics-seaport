CREATE OR REPLACE VIEW V_EXCHANGE_RATES AS
SELECT VARIABLE as QUOTE_CURRENCY_ID,
       VALUE as EXCHANGE_RATE,
       DATE as RATE_DATE
FROM SNOWFLAKE_PUBLIC_DATA_FREE.PUBLIC_DATA_FREE.FX_RATES_TIMESERIES
WHERE VARIABLE IN ('usd_vnd','usd_jpy','usd_eur','usd_cny','usd_krw','usd_sgd','usd_thb','usd_inr','usd_aed','usd_brl','usd_aud','usd_gbp')
QUALIFY ROW_NUMBER() OVER (PARTITION BY VARIABLE ORDER BY DATE DESC) = 1;

CREATE OR REPLACE VIEW V_EXPORT_RESTRICTED_ENTITIES AS
