CREATE OR REPLACE VIEW V_GLOBAL_TRADE_INDEX AS
SELECT REPORTER as COUNTRY, FLOW as DIRECTION, YEAR as PERIOD, VALUE as VALUE_USD
FROM SNOWFLAKE_PUBLIC_DATA_FREE.PUBLIC_DATA_FREE.WTO_TRADE_VALUE
WHERE REPORTER IN ('Vietnam', 'Japan', 'United States', 'China', 'Germany')
  AND YEAR >= 2020;

CREATE OR REPLACE VIEW V_PORT_WEATHER_FORECAST AS
SELECT p.PORT_CODE, p.PORT_NAME, w.DATE_VALID_STD as FORECAST_DATE,
       w.AVG_TEMPERATURE_AIR_2M_F as TEMP_CELSIUS,
       w.AVG_WIND_SPEED_100M_MPH * 1.609 as WIND_SPEED_KMH,
       CASE 
           WHEN w.AVG_WIND_SPEED_100M_MPH > 40 THEN 'Storm Warning'
           WHEN w.AVG_WIND_SPEED_100M_MPH > 25 THEN 'High Wind'
           WHEN w.TOT_PRECIPITATION_IN > 2 THEN 'Heavy Rain'
           ELSE 'Good Weather'
       END as WEATHER_IMPACT
FROM PORT_MASTER p
CROSS JOIN GLOBAL_WEATHER__CLIMATE_DATA_BY_PELMOREX_WEATHER_SOURCE.STANDARD_TILE.FORECAST_DAY w
WHERE w.POSTAL_CODE IN ('100-0001','90001','10001','20095','3011')
  AND w.DATE_VALID_STD BETWEEN CURRENT_DATE() AND DATEADD(day, 7, CURRENT_DATE());

CREATE OR REPLACE VIEW V_VIETNAM_TRADE_STATS AS
SELECT * FROM V_GLOBAL_TRADE_INDEX WHERE COUNTRY = 'Vietnam';

