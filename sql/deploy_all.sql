CREATE OR REPLACE VIEW V_AI_DAILY_COST AS
SELECT DATE_TRUNC('DAY', CALL_TIMESTAMP)::DATE as DAY,
       COUNT(*) as TOTAL_CALLS,
       SUM(TOTAL_TOKENS) as TOTAL_TOKENS,
       SUM(INPUT_TOKENS) as INPUT_TOKENS,
       SUM(OUTPUT_TOKENS) as OUTPUT_TOKENS,
       ROUND(AVG(LATENCY_MS), 0) as AVG_LATENCY_MS,
       ROUND(SUM(TOTAL_TOKENS) * 0.000001, 6) as ESTIMATED_COST_USD,
       SUM(CASE WHEN STATUS LIKE '%FAIL%' THEN 1 ELSE 0 END) as ERRORS
FROM AI_CALL_LOG
GROUP BY DAY ORDER BY DAY DESC;

CREATE OR REPLACE VIEW V_AI_USAGE_SUMMARY AS
SELECT PROCEDURE_NAME,
       COUNT(*) as CALL_COUNT,
       SUM(TOTAL_TOKENS) as TOTAL_TOKENS,
       ROUND(AVG(LATENCY_MS), 0) as AVG_LATENCY_MS,
       SUM(CASE WHEN STATUS LIKE '%SUCCESS%' THEN 1 ELSE 0 END) as SUCCESS_COUNT,
       SUM(CASE WHEN STATUS LIKE '%FAIL%' THEN 1 ELSE 0 END) as ERROR_COUNT,
       ROUND(SUM(CASE WHEN STATUS LIKE '%FAIL%' THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 1) as ERROR_RATE_PCT
FROM AI_CALL_LOG
GROUP BY PROCEDURE_NAME;

