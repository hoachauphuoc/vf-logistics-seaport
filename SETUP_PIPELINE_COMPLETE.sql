-- Comprehensive pipeline monitoring view
CREATE OR REPLACE VIEW LOGISTICS_DB.COMMON.V_PIPELINE_MONITORING AS
SELECT 
    p1.BL_NUMBER, p1.CONTAINER_NUMBER, p1.EXTRACT_ID,
    
    -- Phase 1 Status
    p1.PROCESSING_STATUS AS PHASE1_STATUS,
    p1.REVIEW_STATUS AS PHASE1_REVIEW,
    p1.CONFIDENCE_SCORE AS PHASE1_AI_CONFIDENCE,
    p1.CREATED_AT AS PHASE1_CREATED_AT,
    p1.SYNC_TO_PHASE2_AT AS PHASE1_SYNCED_AT,
    
    -- Phase 2 Status
    p2.TRANSACTION_ID,
    p2.PROCESSING_STATUS AS PHASE2_STATUS,
    p2.GATE_IN_TIME AS PHASE2_GATE_IN,
    p2.ASSIGNED_LOCATION AS PHASE2_LOCATION,
    p2.SYNC_TO_PHASE3_AT AS PHASE2_SYNCED_AT,
    
    -- Phase 3 Status
    p3.INVENTORY_ID,
    p3.PROCESSING_STATUS AS PHASE3_STATUS,
    p3.WAREHOUSE_CODE AS PHASE3_WAREHOUSE,
    p3.STOCK_STATUS AS PHASE3_STOCK_STATUS,
    p3.SYNC_TO_PHASE4_AT AS PHASE3_SYNCED_AT,
    
    -- Phase 4 Status
    p4.SAP_INTEGRATION_ID,
    p4.SAP_SYNC_STATUS AS PHASE4_SAP_STATUS,
    p4.SAP_INVOICE_NUMBER AS PHASE4_INVOICE,
    p4.FULLY_INTEGRATED AS PHASE4_COMPLETE,
    p4.LAST_SYNC_SUCCESS_AT AS PHASE4_SAP_SYNCED_AT,
    
    -- Overall Pipeline Health
    CASE 
        WHEN p4.FULLY_INTEGRATED = TRUE THEN 'COMPLETED'
        WHEN p4.SAP_INTEGRATION_ID IS NOT NULL THEN 'IN_PHASE4'
        WHEN p3.INVENTORY_ID IS NOT NULL THEN 'IN_PHASE3'
        WHEN p2.TRANSACTION_ID IS NOT NULL THEN 'IN_PHASE2'
        WHEN p1.EXTRACT_ID IS NOT NULL THEN 'IN_PHASE1'
        ELSE 'UNKNOWN'
    END AS PIPELINE_STAGE,
    
    DATEDIFF(HOUR, p1.CREATED_AT, 
        COALESCE(p4.LAST_SYNC_SUCCESS_AT, p3.SYNC_TO_PHASE4_AT, 
                 p2.SYNC_TO_PHASE3_AT, CURRENT_TIMESTAMP())
    ) AS TOTAL_PROCESSING_HOURS,
    
    -- Bottleneck Detection
    CASE 
        WHEN p1.NEEDS_HUMAN_REVIEW = TRUE AND p1.REVIEW_STATUS = 'PENDING' 
            THEN 'STUCK_AT_REVIEW'
        WHEN p1.REVIEW_STATUS = 'APPROVED' AND p2.TRANSACTION_ID IS NULL 
             AND DATEDIFF(HOUR, p1.REVIEWED_AT, CURRENT_TIMESTAMP()) > 2 
            THEN 'PHASE1_TO_PHASE2_DELAY'
        WHEN p2.GATE_IN_TIME IS NOT NULL AND p3.INVENTORY_ID IS NULL 
             AND DATEDIFF(HOUR, p2.GATE_IN_TIME, CURRENT_TIMESTAMP()) > 1 
            THEN 'PHASE2_TO_PHASE3_DELAY'
        WHEN p3.STOCK_STATUS = 'IN_STOCK' AND p4.SAP_INTEGRATION_ID IS NULL 
             AND DATEDIFF(HOUR, p3.STOCK_IN_DATE, CURRENT_TIMESTAMP()) > 4 
            THEN 'PHASE3_TO_PHASE4_DELAY'
        WHEN p4.SAP_SYNC_STATUS = 'FAILED' 
            THEN 'SAP_SYNC_FAILED'
        ELSE 'ON_TRACK'
    END AS BOTTLENECK_FLAG
FROM LOGISTICS_DB.PHASE1_SCHEMA.BL_EXTRACTS p1
LEFT JOIN LOGISTICS_DB.PHASE2_SCHEMA.GATE_TRANSACTIONS p2 
    ON p1.EXTRACT_ID = p2.EXTRACT_ID
LEFT JOIN LOGISTICS_DB.PHASE3_SCHEMA.WAREHOUSE_INVENTORY p3 
    ON p2.TRANSACTION_ID = p3.TRANSACTION_ID
LEFT JOIN LOGISTICS_DB.PHASE4_SCHEMA.SAP_INTEGRATION p4 
    ON p3.INVENTORY_ID = p4.INVENTORY_ID;

-- Executive Dashboard with KPIs
CREATE OR REPLACE VIEW LOGISTICS_DB.COMMON.V_EXECUTIVE_DASHBOARD AS
SELECT 
    CURRENT_DATE() AS REPORT_DATE,
    
    -- Phase 1 Metrics
    COUNT(DISTINCT p1.EXTRACT_ID) AS TOTAL_BL_EXTRACTED,
    SUM(CASE WHEN p1.CONFIDENCE_SCORE >= 85 THEN 1 ELSE 0 END) AS AUTO_APPROVED_COUNT,
    SUM(CASE WHEN p1.NEEDS_HUMAN_REVIEW = TRUE THEN 1 ELSE 0 END) AS HUMAN_REVIEW_COUNT,
    ROUND(AVG(p1.CONFIDENCE_SCORE), 2) AS AVG_AI_CONFIDENCE,
    
    -- Phase 2 Metrics
    COUNT(DISTINCT p2.TRANSACTION_ID) AS TOTAL_GATE_TRANSACTIONS,
    SUM(CASE WHEN p2.TRANSACTION_TYPE = 'GATE_IN' THEN 1 ELSE 0 END) AS GATE_IN_COUNT,
    SUM(CASE WHEN p2.TRANSACTION_TYPE = 'GATE_OUT' THEN 1 ELSE 0 END) AS GATE_OUT_COUNT,
    ROUND(AVG(p2.DURATION_MINUTES), 2) AS AVG_GATE_DURATION_MIN,
    
    -- Phase 3 Metrics
    COUNT(DISTINCT p3.INVENTORY_ID) AS TOTAL_INVENTORY_ITEMS,
    COUNT(DISTINCT p3.WAREHOUSE_CODE) AS ACTIVE_WAREHOUSES,
    SUM(CASE WHEN p3.STOCK_STATUS = 'IN_STOCK' THEN 1 ELSE 0 END) AS CURRENT_STOCK_COUNT,
    SUM(p3.RESTACKING_COUNT) AS TOTAL_RESTACKING_OPERATIONS,
    
    -- Phase 4 Metrics
    COUNT(DISTINCT p4.SAP_INTEGRATION_ID) AS TOTAL_SAP_RECORDS,
    SUM(CASE WHEN p4.SAP_SYNC_STATUS = 'SUCCESS' THEN 1 ELSE 0 END) AS SAP_SUCCESS_COUNT,
    SUM(CASE WHEN p4.SAP_SYNC_STATUS = 'FAILED' THEN 1 ELSE 0 END) AS SAP_FAILED_COUNT,
    SUM(CASE WHEN p4.FULLY_INTEGRATED = TRUE THEN 1 ELSE 0 END) AS FULLY_INTEGRATED_COUNT,
    ROUND(SUM(p4.INVOICE_AMOUNT), 2) AS TOTAL_INVOICE_AMOUNT_VND,
    
    -- Overall Pipeline Health
    ROUND(
        SUM(CASE WHEN p4.FULLY_INTEGRATED = TRUE THEN 1 ELSE 0 END) * 100.0 / 
        NULLIF(COUNT(DISTINCT p1.EXTRACT_ID), 0), 
    2) AS END_TO_END_COMPLETION_RATE,
    
    -- Business Value Metrics
    ROUND(COUNT(DISTINCT p1.EXTRACT_ID) * 45 / 60.0, 2) AS MANUAL_HOURS_SAVED,
    ROUND((COUNT(DISTINCT p2.TRANSACTION_ID) * 13500000000.0 / 365 / 3000), 0) AS ANONYMOUS_PORTAL_SAVINGS_VND
FROM LOGISTICS_DB.PHASE1_SCHEMA.BL_EXTRACTS p1
LEFT JOIN LOGISTICS_DB.PHASE2_SCHEMA.GATE_TRANSACTIONS p2 
    ON p1.EXTRACT_ID = p2.EXTRACT_ID
LEFT JOIN LOGISTICS_DB.PHASE3_SCHEMA.WAREHOUSE_INVENTORY p3 
    ON p2.TRANSACTION_ID = p3.TRANSACTION_ID
LEFT JOIN LOGISTICS_DB.PHASE4_SCHEMA.SAP_INTEGRATION p4 
    ON p3.INVENTORY_ID = p4.INVENTORY_ID
WHERE p1.CREATED_AT >= DATEADD(DAY, -30, CURRENT_DATE());

