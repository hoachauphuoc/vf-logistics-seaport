-- STEP 4: CREATE TASKS (Automated Sync Pipeline)
-- =====================================================

-- TASK 1: Sync Phase 1 → Phase 2
CREATE OR REPLACE TASK LOGISTICS_DB.COMMON.SYNC_PHASE1_TO_PHASE2
    WAREHOUSE = COMPUTE_WH
    SCHEDULE = '5 MINUTE'
    WHEN SYSTEM$STREAM_HAS_DATA('LOGISTICS_DB.PHASE1_SCHEMA.BL_EXTRACTS_STREAM')
AS
INSERT INTO LOGISTICS_DB.PHASE2_SCHEMA.GATE_TRANSACTIONS (
    TRANSACTION_ID, CONTAINER_NUMBER, BL_NUMBER, EXTRACT_ID,
    CONTAINER_TYPE, PROCESSING_STATUS, CREATED_AT, CREATED_BY
)
SELECT 
    'TXN-' || EXTRACT_ID || '-' || UNIFORM(1000, 9999, RANDOM())::VARCHAR,
    CONTAINER_NUMBER, BL_NUMBER, EXTRACT_ID,
    'STANDARD', 'AWAITING_GATE_IN',
    CURRENT_TIMESTAMP(), 'SYSTEM_AUTO_SYNC'
FROM LOGISTICS_DB.PHASE1_SCHEMA.BL_EXTRACTS_STREAM
WHERE METADATA$ACTION = 'INSERT' 
  AND REVIEW_STATUS = 'APPROVED'
  AND PROCESSING_STATUS = 'REVIEWED'
  AND SYNC_TO_PHASE2_AT IS NULL;

-- TASK 2: Sync Phase 2 → Phase 3
CREATE OR REPLACE TASK LOGISTICS_DB.COMMON.SYNC_PHASE2_TO_PHASE3
    WAREHOUSE = COMPUTE_WH
    SCHEDULE = '5 MINUTE'
    WHEN SYSTEM$STREAM_HAS_DATA('LOGISTICS_DB.PHASE2_SCHEMA.GATE_TRANSACTIONS_STREAM')
AS
INSERT INTO LOGISTICS_DB.PHASE3_SCHEMA.WAREHOUSE_INVENTORY (
    INVENTORY_ID, CONTAINER_NUMBER, BL_NUMBER, EXTRACT_ID, TRANSACTION_ID,
    WAREHOUSE_CODE, LOCATION_CODE, STOCK_STATUS, PROCESSING_STATUS,
    CREATED_AT, CREATED_BY
)
SELECT 
    'INV-' || TRANSACTION_ID || '-' || UNIFORM(1000, 9999, RANDOM())::VARCHAR,
    CONTAINER_NUMBER, BL_NUMBER, EXTRACT_ID, TRANSACTION_ID,
    'WH-' || UNIFORM(1, 7, RANDOM())::VARCHAR,
    ASSIGNED_LOCATION, 'IN_STOCK', 'AWAITING_ALLOCATION',
    CURRENT_TIMESTAMP(), 'SYSTEM_AUTO_SYNC'
FROM LOGISTICS_DB.PHASE2_SCHEMA.GATE_TRANSACTIONS_STREAM
WHERE METADATA$ACTION = 'INSERT' 
  AND TRANSACTION_TYPE = 'GATE_IN'
  AND GATE_IN_TIME IS NOT NULL
  AND SYNC_TO_PHASE3_AT IS NULL;

-- TASK 3: Sync Phase 3 → Phase 4
CREATE OR REPLACE TASK LOGISTICS_DB.COMMON.SYNC_PHASE3_TO_PHASE4
    WAREHOUSE = COMPUTE_WH
    SCHEDULE = '5 MINUTE'
    WHEN SYSTEM$STREAM_HAS_DATA('LOGISTICS_DB.PHASE3_SCHEMA.WAREHOUSE_INVENTORY_STREAM')
AS
INSERT INTO LOGISTICS_DB.PHASE4_SCHEMA.SAP_INTEGRATION (
    SAP_INTEGRATION_ID, BL_NUMBER, EXTRACT_ID, TRANSACTION_ID,
    INVENTORY_ID, CONTAINER_NUMBER, SAP_SYNC_STATUS, PROCESSING_STATUS,
    CREATED_AT, CREATED_BY
)
SELECT 
    'SAP-' || INVENTORY_ID || '-' || UNIFORM(1000, 9999, RANDOM())::VARCHAR,
    BL_NUMBER, EXTRACT_ID, TRANSACTION_ID,
    INVENTORY_ID, CONTAINER_NUMBER, 'PENDING', 'READY_FOR_SAP',
    CURRENT_TIMESTAMP(), 'SYSTEM_AUTO_SYNC'
FROM LOGISTICS_DB.PHASE3_SCHEMA.WAREHOUSE_INVENTORY_STREAM
WHERE METADATA$ACTION = 'INSERT' 
  AND STOCK_STATUS = 'IN_STOCK'
  AND SYNC_TO_PHASE4_AT IS NULL;

-- TASK 4: Update Phase 1 sync status
CREATE OR REPLACE TASK LOGISTICS_DB.COMMON.UPDATE_PHASE1_SYNC_STATUS
    WAREHOUSE = COMPUTE_WH
    SCHEDULE = '5 MINUTE'
AS
MERGE INTO LOGISTICS_DB.PHASE1_SCHEMA.BL_EXTRACTS t
USING (
    SELECT DISTINCT EXTRACT_ID
    FROM LOGISTICS_DB.PHASE2_SCHEMA.GATE_TRANSACTIONS
    WHERE CREATED_AT >= DATEADD(MINUTE, -10, CURRENT_TIMESTAMP())
      AND CREATED_BY = 'SYSTEM_AUTO_SYNC'
) s ON t.EXTRACT_ID = s.EXTRACT_ID
WHEN MATCHED AND t.SYNC_TO_PHASE2_AT IS NULL THEN
    UPDATE SET 
        SYNC_TO_PHASE2_AT = CURRENT_TIMESTAMP(),
        PROCESSING_STATUS = 'SYNCED_TO_PHASE2',
        UPDATED_AT = CURRENT_TIMESTAMP(),
        UPDATED_BY = 'SYSTEM_AUTO_UPDATE';

-- TASK 5: Update Phase 2 sync status
CREATE OR REPLACE TASK LOGISTICS_DB.COMMON.UPDATE_PHASE2_SYNC_STATUS
    WAREHOUSE = COMPUTE_WH
    SCHEDULE = '5 MINUTE'
AS
MERGE INTO LOGISTICS_DB.PHASE2_SCHEMA.GATE_TRANSACTIONS t
USING (
    SELECT DISTINCT TRANSACTION_ID
    FROM LOGISTICS_DB.PHASE3_SCHEMA.WAREHOUSE_INVENTORY
    WHERE CREATED_AT >= DATEADD(MINUTE, -10, CURRENT_TIMESTAMP())
      AND CREATED_BY = 'SYSTEM_AUTO_SYNC'
) s ON t.TRANSACTION_ID = s.TRANSACTION_ID
WHEN MATCHED AND t.SYNC_TO_PHASE3_AT IS NULL THEN
    UPDATE SET 
        SYNC_TO_PHASE3_AT = CURRENT_TIMESTAMP(),
        PROCESSING_STATUS = 'SYNCED_TO_PHASE3',
        UPDATED_AT = CURRENT_TIMESTAMP(),
        UPDATED_BY = 'SYSTEM_AUTO_UPDATE';

-- TASK 6: Update Phase 3 sync status
CREATE OR REPLACE TASK LOGISTICS_DB.COMMON.UPDATE_PHASE3_SYNC_STATUS
    WAREHOUSE = COMPUTE_WH
    SCHEDULE = '5 MINUTE'
AS
MERGE INTO LOGISTICS_DB.PHASE3_SCHEMA.WAREHOUSE_INVENTORY t
USING (
    SELECT DISTINCT INVENTORY_ID
    FROM LOGISTICS_DB.PHASE4_SCHEMA.SAP_INTEGRATION
    WHERE CREATED_AT >= DATEADD(MINUTE, -10, CURRENT_TIMESTAMP())
      AND CREATED_BY = 'SYSTEM_AUTO_SYNC'
) s ON t.INVENTORY_ID = s.INVENTORY_ID
WHEN MATCHED AND t.SYNC_TO_PHASE4_AT IS NULL THEN
    UPDATE SET 
        SYNC_TO_PHASE4_AT = CURRENT_TIMESTAMP(),
        PROCESSING_STATUS = 'SYNCED_TO_PHASE4',
        STOCK_STATUS = 'SYNCED_TO_PHASE4',
        UPDATED_AT = CURRENT_TIMESTAMP(),
        UPDATED_BY = 'SYSTEM_AUTO_UPDATE';

